// ── API publique ──────────────────────────────────────────────────────────────
let _currentLang = localStorage.getItem('lang') || 'fr';

function t(key) {
    return (TRANSLATIONS[_currentLang]||TRANSLATIONS.fr)[key]
        || TRANSLATIONS.fr[key]
        || key;
}

function applyLang(lang) {
    if (!TRANSLATIONS[lang]) return;
    _currentLang = lang;
    localStorage.setItem('lang', lang);
    const user = JSON.parse(localStorage.getItem('user')||'{}');
    user.lang = lang; localStorage.setItem('user', JSON.stringify(user));
    applyTranslationsToDOM();
}

function initLang() {
    const user = JSON.parse(localStorage.getItem('user')||'{}');
    _currentLang = user.lang || localStorage.getItem('lang') || 'fr';
    const sel = document.getElementById('prof-lang');
    if (sel) sel.value = _currentLang;
    applyTranslationsToDOM();
}

function applyTranslationsToDOM() {
    // 1. Éléments avec data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const val = t(key);
        if (['INPUT','TEXTAREA'].includes(el.tagName)) el.placeholder = val;
        else el.textContent = val;
    });

    // 2. Tabs de navigation
    const tabLabels = ['archives','params','groups','chat'];
    document.querySelectorAll('.tab-item').forEach((item, i) => {
        const span = item.querySelector('span:last-child');
        if (span && tabLabels[i]) span.textContent = t(tabLabels[i]);
    });

    // 3. Input message
    const mi = document.getElementById('msg-input');
    if (mi) mi.placeholder = t('writeMsg');

    // 4. Titre de la page courante
    const titles = [t('archives'), t('params'), t('myGroups'), t('chat'), t('prep')];
    const titleEl = document.getElementById('page-title');
    if (titleEl && typeof currentPage !== 'undefined' && titles[currentPage]) {
        titleEl.textContent = titles[currentPage];
    }

    // 5. Accordéon Résumé
    const summaryLabel = document.querySelector('label[for="check-eink"].acc-label-text');
    if (summaryLabel) summaryLabel.textContent = t('summary');

    // 6. Alerte date
    const alertEl = document.getElementById('order-date-alert');
    if (alertEl) alertEl.textContent = t('noDateAlert');

    // 7. Page Params : labels accordéons
    const accMap = {
        'check-profil': 'profile',
        'check-mdp':    'password',
        'check-skin':   'appearance',
    };
    Object.entries(accMap).forEach(([id, key]) => {
        const label = document.querySelector(`label[for="${id}"].acc-label-text`);
        if (label) label.textContent = t(key);
    });

    // 8. Labels page Params (champs statiques)
    const fieldMap = {
        'prof-firstname': 'firstname', 'prof-lastname': 'lastname',
        'prof-email': 'emailLabel',    'prof-phone': 'phone',
        'prof-lang': 'lang',
    };
    Object.entries(fieldMap).forEach(([id, key]) => {
        const el = document.getElementById(id);
        if (el) el.placeholder = t(key);
        // Label au-dessus
        const prev = el?.previousElementSibling;
        if (prev && prev.tagName === 'LABEL') prev.textContent = t(key);
    });

    // 9. Boutons page Params
    const btnMap = {
        'btn-save-profile': 'saveProfile',
        'btn-change-pwd':   'changePwd',
    };
    Object.entries(btnMap).forEach(([id, key]) => {
        const el = document.getElementById(id);
        if (el) el.textContent = t(key);
    });

    // 10. Labels sélecteur thème
    const skinBtns = document.querySelectorAll('#skin-selector .skin-btn');
    if (skinBtns.length >= 3) {
        skinBtns[0].textContent = t('themeDefault');
        skinBtns[1].textContent = t('themeSlate');
        skinBtns[2].textContent = t('themeCustom');
    }

    // 11. Sélecteur langue profil
    const langSel = document.getElementById('prof-lang');
    if (langSel) langSel.value = _currentLang;
}
